package if_else;

public class pythagoras {
    public static void main(String args[]){
        int a=2,b=2,c=2;

        if((a*a) +(b*b) == c*c){
           System.out.println("its a pythagorean triplet");
        }else{
            System.out.println("its not a Pythagorean triplet");
        }
    }
}
