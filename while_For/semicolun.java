package while_For;

public class semicolun {
    public static void main(String args[]){
        int x = 10;
        int i=1;
        while(i<=10) i++;{
            System.out.println("while: x = "+x);
            
        }

        for(i=1;i<=10;i++);{
            System.out.println("For: x = "+x);
        }
    }
}
